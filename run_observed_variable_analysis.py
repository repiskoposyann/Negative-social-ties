def add_box(ax, x, y, w, h, text, fontsize=11, lw=1.4):
    """Add rounded annotation box in axes coordinates."""
    patch = FancyBboxPatch(
        (x, y), w, h,
        boxstyle="round,pad=0.02,rounding_size=0.02",
        linewidth=lw,
        edgecolor="black",
        facecolor="white",
        transform=ax.transAxes,
        clip_on=False,
    )
    ax.add_patch(patch)
    ax.text(x + w / 2, y + h / 2, text, ha="center", va="center", fontsize=fontsize, transform=ax.transAxes)


def add_arrow(ax, x1, y1, x2, y2, text=None, tx=None, ty=None, lw=1.5, ls="-", fontsize=10):
    """Add arrow with optional label."""
    ax.annotate(
        "",
        xy=(x2, y2), xytext=(x1, y1),
        xycoords=ax.transAxes, textcoords=ax.transAxes,
        arrowprops=dict(arrowstyle="->", lw=lw, linestyle=ls, color="black"),
    )
    if text is not None:
        tx = (x1 + x2) / 2 if tx is None else tx
        ty = (y1 + y2) / 2 if ty is None else ty
        ax.text(tx, ty, text, ha="center", va="center", fontsize=fontsize, transform=ax.transAxes)

