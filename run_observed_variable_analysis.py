def make_figure3(df: pd.DataFrame, config: dict):
    """Simple slopes moderation figure for cyberbullying × family support."""
    plot_df = df[[config["cyber_col"], config["family_col"], config["strong_withdraw_col"], config["neutral_withdraw_col"]]].copy()
    plot_df["withdrawal_T2"] = plot_df[[config["strong_withdraw_col"], config["neutral_withdraw_col"]]].mean(axis=1)

    plot_df["withdrawal_z"] = zscore(plot_df["withdrawal_T2"])
    plot_df["cyber_z"] = zscore(plot_df[config["cyber_col"]])
    plot_df["family_z"] = zscore(plot_df[config["family_col"]])
    plot_df["interaction"] = plot_df["cyber_z"] * plot_df["family_z"]

    plot_df = plot_df[["withdrawal_z", "cyber_z", "family_z", "interaction"]].dropna()
    if plot_df.shape[0] < 20:
        return

    X = add_constant(plot_df[["cyber_z", "family_z", "interaction"]])
    model = sm.OLS(plot_df["withdrawal_z"], X).fit()

    xs = np.linspace(plot_df["cyber_z"].min(), plot_df["cyber_z"].max(), 200)
    fam_low = -1.0
    fam_high = 1.0

    y_low = (
        model.params["const"]
        + model.params["cyber_z"] * xs
        + model.params["family_z"] * fam_low
        + model.params["interaction"] * xs * fam_low
    )
    y_high = (
        model.params["const"]
        + model.params["cyber_z"] * xs
        + model.params["family_z"] * fam_high
        + model.params["interaction"] * xs * fam_high
    )

    # Simple slopes and p-values
    b1 = model.params["cyber_z"]
    b3 = model.params["interaction"]
    cov = model.cov_params()

    slope_low = b1 + b3 * fam_low
    slope_high = b1 + b3 * fam_high

    var_low = (
        cov.loc["cyber_z", "cyber_z"]
        + fam_low**2 * cov.loc["interaction", "interaction"]
        + 2 * fam_low * cov.loc["cyber_z", "interaction"]
    )
    var_high = (
        cov.loc["cyber_z", "cyber_z"]
        + fam_high**2 * cov.loc["interaction", "interaction"]
        + 2 * fam_high * cov.loc["cyber_z", "interaction"]
    )

    se_low = np.sqrt(var_low) if var_low > 0 else np.nan
    se_high = np.sqrt(var_high) if var_high > 0 else np.nan

    t_low = slope_low / se_low if pd.notna(se_low) and se_low > 0 else np.nan
    t_high = slope_high / se_high if pd.notna(se_high) and se_high > 0 else np.nan

    df_resid = model.df_resid
    p_low = 2 * st.t.sf(np.abs(t_low), df_resid) if pd.notna(t_low) else np.nan
    p_high = 2 * st.t.sf(np.abs(t_high), df_resid) if pd.notna(t_high) else np.nan

    fig, ax = plt.subplots(figsize=(9.2, 6.8))
    ax.plot(xs, y_low, linewidth=2.2, label="Low family support (-1 SD)")
    ax.plot(xs, y_high, linewidth=2.0, linestyle="--", label="High family support (+1 SD)")

    ax.set_xlabel("Cyberbullying (standardized)")
    ax.set_ylabel("Predicted Withdrawal (standardized)")
    ax.set_title("Simple Slopes for the Moderating Role of Family Support", pad=12)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.legend(frameon=False, loc="upper left")

    txt = (
        rf"Interaction: $\beta={model.params['interaction']:.2f},\ p{pretty_p(model.pvalues['interaction'])}$" "\n"
        rf"Low support slope: $\beta={slope_low:.2f},\ p{pretty_p(p_low)}$" "\n"
        rf"High support slope: $\beta={slope_high:.2f},\ p{pretty_p(p_high)}$"
    )
    txt = txt.replace("0.", ".").replace("-0.", "-.")

    ax.text(
        0.98, 0.07, txt,
        transform=ax.transAxes,
        ha="right", va="bottom", fontsize=10.5
    )

    fig.savefig(FIG_DIR / "figure3_simple_slopes.png", bbox_inches="tight", facecolor="white")
    plt.close(fig)

