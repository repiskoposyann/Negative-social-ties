def bootstrap_indirect_effects(df: pd.DataFrame, config: dict) -> pd.DataFrame:
    """Bootstrap indirect effects using refit models on resamples."""
    rng = np.random.default_rng(config["bootstrap_seed"])
    n = len(df)
    rows = []

    for _ in range(config["n_boot"]):
        sample_idx = rng.integers(0, n, size=n)
        sample = df.iloc[sample_idx].copy()

        sample["withdrawal_T2"] = sample[
            [config["strong_withdraw_col"], config["neutral_withdraw_col"]]
        ].mean(axis=1)
        sample["offline_z"] = zscore(sample[config["offline_col"]])
        sample["cyber_z"] = zscore(sample[config["cyber_col"]])
        sample["family_z"] = zscore(sample[config["family_col"]])
        sample["offline_x_family"] = sample["offline_z"] * sample["family_z"]
        sample["cyber_x_family"] = sample["cyber_z"] * sample["family_z"]

        try:
            withdrawal_model, _ = fit_withdrawal_model(sample, config)
            dep_model, _ = fit_outcome_model(sample, config, config["dep_t3_col"], config["dep_t1_col"])
            anx_model, _ = fit_outcome_model(sample, config, config["anx_t3_col"], config["anx_t1_col"])
            stress_model, _ = fit_outcome_model(sample, config, config["stress_t3_col"], config["stress_t1_col"])

            a_off = withdrawal_model.params.get(config["offline_col"], np.nan)
            a_cyb = withdrawal_model.params.get(config["cyber_col"], np.nan)

            b_dep = dep_model.params.get("withdrawal_T2", np.nan)
            b_anx = anx_model.params.get("withdrawal_T2", np.nan)
            b_stress = stress_model.params.get("withdrawal_T2", np.nan)

            rows.append({
                "offline_ind_dep": a_off * b_dep,
                "offline_ind_anx": a_off * b_anx,
                "offline_ind_stress": a_off * b_stress,
                "cyber_ind_dep": a_cyb * b_dep,
                "cyber_ind_anx": a_cyb * b_anx,
                "cyber_ind_stress": a_cyb * b_stress,
                "offline_to_withdrawal": a_off,
                "cyber_to_withdrawal": a_cyb,
            })
        except Exception:
            continue

    boot = pd.DataFrame(rows)
    if boot.empty:
        raise RuntimeError("Bootstrap failed for all resamples.")

    summary_rows = []
    for col in boot.columns:
        arr = boot[col].dropna().values
        ci_low, ci_high = np.percentile(arr, [2.5, 97.5])
        prop_le_zero = np.mean(arr <= 0)
        prop_ge_zero = np.mean(arr >= 0)
        p_two = min(1.0, 2 * min(prop_le_zero, prop_ge_zero))

        summary_rows.append({
            "Effect": col,
            "Boot_N": len(arr),
            "Mean": np.mean(arr),
            "SE": np.std(arr, ddof=1),
            "CI_low": ci_low,
            "CI_high": ci_high,
            "p_two_tailed_approx": p_two,
        })

    return pd.DataFrame(summary_rows)


def build_wald_table(withdrawal_model, config: dict) -> pd.DataFrame:
    """Proper Wald-style comparison from the fitted withdrawal model covariance matrix."""
    b_off = withdrawal_model.params.get(config["offline_col"], np.nan)
    b_cyb = withdrawal_model.params.get(config["cyber_col"], np.nan)

    cov = withdrawal_model.cov_params()

    if (
        pd.isna(b_off)
        or pd.isna(b_cyb)
        or config["offline_col"] not in cov.index
        or config["cyber_col"] not in cov.index
    ):
        return pd.DataFrame([{
            "Contrast": "Cyber vs Offline on Withdrawal",
            "Diff_mean": np.nan,
            "Diff_SE": np.nan,
            "Wald_chi2_1df": np.nan,
            "p_value": np.nan,
            "CI_low": np.nan,
            "CI_high": np.nan,
        }])

    var_off = cov.loc[config["offline_col"], config["offline_col"]]
    var_cyb = cov.loc[config["cyber_col"], config["cyber_col"]]
    cov_off_cyb = cov.loc[config["offline_col"], config["cyber_col"]]

    diff = b_cyb - b_off
    var_diff = var_cyb + var_off - 2 * cov_off_cyb

    if pd.isna(var_diff) or var_diff <= 0:
        diff_se = np.nan
        wald = np.nan
        p_value = np.nan
        ci_low = np.nan
        ci_high = np.nan
    else:
        diff_se = float(np.sqrt(var_diff))
        wald = float((diff / diff_se) ** 2)
        p_value = float(st.chi2.sf(wald, 1))
        ci_low = float(diff - 1.96 * diff_se)
        ci_high = float(diff + 1.96 * diff_se)

    return pd.DataFrame([{
        "Contrast": "Cyber vs Offline on Withdrawal",
        "Diff_mean": float(diff),
        "Diff_SE": diff_se,
        "Wald_chi2_1df": wald,
        "p_value": p_value,
        "CI_low": ci_low,
        "CI_high": ci_high,
    }])


def rename_bootstrap_effects(boot_df: pd.DataFrame) -> pd.DataFrame:
    """Human-readable names for bootstrap effects."""
    mapping = {
        "offline_ind_dep": "Offline bullying -> Withdrawal -> Depression",
        "offline_ind_anx": "Offline bullying -> Withdrawal -> Anxiety",
        "offline_ind_stress": "Offline bullying -> Withdrawal -> Stress",
        "cyber_ind_dep": "Cyberbullying -> Withdrawal -> Depression",
        "cyber_ind_anx": "Cyberbullying -> Withdrawal -> Anxiety",
        "cyber_ind_stress": "Cyberbullying -> Withdrawal -> Stress",
        "offline_to_withdrawal": "Offline bullying -> Withdrawal",
        "cyber_to_withdrawal": "Cyberbullying -> Withdrawal",
    }
    out = boot_df.copy()
    out["Effect"] = out["Effect"].map(mapping).fillna(out["Effect"])
    return out

