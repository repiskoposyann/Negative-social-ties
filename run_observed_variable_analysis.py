def fit_ols(y: pd.Series, X: pd.DataFrame):
    """Fit OLS after dropping missing rows."""
    data = pd.concat([y, X], axis=1).dropna()
    y_clean = data.iloc[:, 0]
    X_clean = add_constant(data.iloc[:, 1:])
    model = sm.OLS(y_clean, X_clean).fit()
    return model, data


def summarize_model(model, label: str) -> pd.DataFrame:
    """Turn model output into a compact DataFrame."""
    rows = []
    for term in model.params.index:
        rows.append({
            "Model": label,
            "Term": term,
            "B": model.params[term],
            "SE": model.bse[term],
            "t": model.tvalues[term],
            "p": model.pvalues[term],
        })
    return pd.DataFrame(rows)


def standardized_beta_from_model(model, data: pd.DataFrame, x_name: str, y_name: str) -> float:
    """Convert an unstandardized coefficient to a standardized beta."""
    if x_name not in model.params.index or x_name not in data.columns or y_name not in data.columns:
        return np.nan

    x = pd.to_numeric(data[x_name], errors="coerce")
    y = pd.to_numeric(data[y_name], errors="coerce")
    valid = pd.concat([x, y], axis=1).dropna()

    if valid.shape[0] < 5:
        return np.nan

    sx = valid.iloc[:, 0].std(ddof=0)
    sy = valid.iloc[:, 1].std(ddof=0)

    if sx == 0 or sy == 0 or pd.isna(sx) or pd.isna(sy):
        return np.nan

    return float(model.params[x_name] * sx / sy)

