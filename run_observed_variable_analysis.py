def pretty_p(p: float) -> str:
    """Pretty p-value formatter for figures and text."""
    if pd.isna(p):
        return ""
    if p < 0.001:
        return "< .001"
    return f"= {p:.3f}".replace("0.", ".")


def zscore(series: pd.Series) -> pd.Series:
    """Population-standardized z-score."""
    mean = series.mean(skipna=True)
    sd = series.std(skipna=True, ddof=0)
    if pd.isna(sd) or sd == 0:
        return pd.Series(np.nan, index=series.index)
    return (series - mean) / sd


def add_constant(df: pd.DataFrame) -> pd.DataFrame:
    """Add constant safely."""
    return sm.add_constant(df, has_constant="add")


def p_label(p):
    """Mathtext-ready p label."""
    if pd.isna(p):
        return ""
    if p < 0.001:
        return r"$p<.001$"
    return rf"$p={p:.3f}$".replace("0.", ".")


def beta_label(beta):
    """Mathtext-ready beta label."""
    if pd.isna(beta):
        return ""
    return rf"$\beta={beta:.2f}$".replace("0.", ".").replace("-0.", "-.")


